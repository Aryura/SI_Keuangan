    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String jk;
        if (jkl.isSelected()) {
            jk = "Laki-Laki";
        } else {
            jk = "Perempuan";
        }
        try {
            ResultSet ss = sdb.checkNisn(nisn.getText());
            if (ss.next()) {
                JOptionPane.showMessageDialog(null, "Data Siswa sudah ada");
                System.out.println("Data Siswa sudah ada");
            } else {
                sdb.createSiswa(nisn.getText(), nis.getText(), nama_siswa.getText(), jk, temLahir.getText(), format.format(tglLahir.getDate()), kelas.getSelectedItem().toString(), alamat.getText());
                 JOptionPane.showMessageDialog(null, "Data Siswa Berhasil Dibuat");
                tabelSiswa();
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_saveActionPerformed

