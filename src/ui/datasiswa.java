    private void tblsiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblsiswaMouseClicked
        // TODO add your handling code here:
        int bar = tblsiswa.getSelectedRow();
        String nisns = tabmode.getValueAt(bar, 0).toString();
        String niss = tabmode.getValueAt(bar, 1).toString();
        String nsiswa = tabmode.getValueAt(bar, 2).toString();
        String jk = tabmode.getValueAt(bar, 3).toString();
        String tlahir = tabmode.getValueAt(bar, 4).toString();
//        Date tglahir;
        try {
            java.util.Date tglahir = new SimpleDateFormat("yyyy-MM-dd").parse(tabmode.getValueAt(bar, 5).toString());
           tglLahir.setDate(tglahir);
        } catch (ParseException ex) {
            Logger.getLogger(datasiswa.class.getName()).log(Level.SEVERE, null, ex);
        }
        String kel = tabmode.getValueAt(bar, 6).toString();
        String alam = tabmode.getValueAt(bar, 7).toString();
        nisn.setText(nisns);
        nis.setText(niss);
        nama_siswa.setText(nsiswa);
        if (jk.equals("Laki-Laki")) {
            jkl.setSelected(true);
            jkp.setSelected(false);
        } else {
            jkl.setSelected(false);
            jkp.setSelected(true);
        }
        temLahir.setText(tlahir);
//        tglLahir.setDate(tglahir);
        kelas.setSelectedItem(kel);
        alamat.setText(alam);
    }//GEN-LAST:event_tblsiswaMouseClicked

