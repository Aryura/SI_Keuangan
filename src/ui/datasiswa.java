    private void cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariActionPerformed
        // TODO add your handling code here:
        Object[] baris = {"NISN", "NIS", "Nama_SIswa", "Jenis Kelamin","Tempat_Lahir" ,"Tanggal Lahir", "Kelas", "Alamat"};
        tabmode = new DefaultTableModel(null, baris);
        tblsiswa.setModel(tabmode);
        try {
            ResultSet cs = sdb.searchSiswa(SearchField.getText());
            while (cs.next()) {                
                String a = cs.getString("NISN");
                String b = cs.getString("NIS");
                String c = cs.getString("Nama_SIswa");
                String d = cs.getString("Jenis_kelamin");
                String e = cs.getString("Tempat_Lahir");
                String f = cs.getString("Tanggal_Lahir");
                String g= cs.getString("Kelas");
                String h = cs.getString("Alamat");
                Object[] data = {a, b, c, d, e, f, g,h};
                tabmode.addRow(data);
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_cariActionPerformed

