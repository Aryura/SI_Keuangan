    private void combobox() {
        try {
            ResultSet sguru = kdb.showGuru();
            while (sguru.next()) {
                Object[] data = new Object[1];
                data[0] = sguru.getString("nama_guru");
                comboguru.addItem(data[0].toString());
            }
        } catch (SQLException e) {
        }
    }

    private void tabelKelas() {
        Object[] baris = {"Id", "Nama Kelas", "Wali Kelas", "Jumlah Murid"};
        tabmode = new DefaultTableModel(null, baris);
        tblkelas.setModel(tabmode);
        try {
            ResultSet tba = kdb.tabelKelas();
            while (tba.next()) {
//                int a = tba.getInt("id");
                 String a = tba.getString("customID");
                String b = tba.getString("nama_kelas");
                String c = tba.getString("wali_kelas");
                String d = tba.getString("jumlah_murid");
                Object[] data = {a, b, c, d};
                tabmode.addRow(data);
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
    }

