    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        try {
            ResultSet ck = kdb.checkKelas(nkelas.getText());
            if (ck.next()) {
                JOptionPane.showMessageDialog(null,"Data Kelas Sudah Ada");
            } else {
                kdb.createKelas(nkelas.getText(), comboguru.getSelectedItem().toString());
                JOptionPane.showMessageDialog(null,"Data kelas Berhasil Disimpan");
                tabelKelas();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null,"Data kelas Gagal Disimpan"+e.getMessage());
        }

    }//GEN-LAST:event_saveActionPerformed

