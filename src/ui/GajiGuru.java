    private void tblggMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblggMouseClicked
        // TODO add your handling code here:
        int bar = tblgg.getSelectedRow();
        String ids = tabmode.getValueAt(bar, 0).toString();
        String idsg = tabmode.getValueAt(bar, 1).toString();
        String ng = tabmode.getValueAt(bar, 2).toString();
        try {
            java.util.Date tglgg = new SimpleDateFormat("yyyy-MM-dd").parse(tabmode.getValueAt(bar, 3).toString());
            tgl.setDate(tglgg);
        } catch (ParseException ex) {
            Logger.getLogger(datasiswa.class.getName()).log(Level.SEVERE, null, ex);
        }
        String jp = tabmode.getValueAt(bar, 4).toString();
        String gj = tabmode.getValueAt(bar, 5).toString();
        String t = tabmode.getValueAt(bar, 6).toString();
        String kt = tabmode.getValueAt(bar, 7).toString();
        String to = tabmode.getValueAt(bar, 8).toString();
        trx.setText(ids);
        idguru.setText(idsg);
        namaguru.setText(ng);
        jampelajaran.setText(jp);
        gajijam.setText(gj);
        tunjangan.setText(t);
        kettunjangan.setText(kt);
        total.setText(to);
    }//GEN-LAST:event_tblggMouseClicked

    private void cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariActionPerformed
        // TODO add your handling code here:
        Object[] baris = {"Id", "ID Guru", "Nama Guru", "Tanggal", "Jam Pelajaran", "Gaji/jam", "Tunjangan", "Keterangan Tunjangan", "total"};
        tabmode = new DefaultTableModel(null, baris);
        tblgg.setModel(tabmode);
        try {
            ResultSet gg = gdb.searchGajiGuru(tcari.getText());
            while (gg.next()) {
                String cid = gg.getString("costumID");
                int ig = gg.getInt("id_guru");
                String ng = gg.getString("nama_guru");
                String tgll = gg.getString("tanggal");
                int jampel = gg.getInt("jam_pelajaran");
                double gj = gg.getDouble("gaji_jam");
                double tun = gg.getDouble("tunjangan");
                String kettun = gg.getString("ket_tunjangan");
                int tot = gg.getInt("total");
                Object[] data = {cid, ig, ng, tgll, jampel, gj, tun, kettun, tot};
                tabmode.addRow(data);
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_cariActionPerformed

