    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        try {
            ResultSet cg = gdb.checkGajiGuru(namaguru.getText());
            if (cg.next()) {
                JOptionPane.showMessageDialog(null,"Data Guru sudah ada");
            } else {

                gdb.createGajiGuru(Integer.parseInt(idguru.getText()), namaguru.getText(), format.format(tgl.getDate()), Integer.parseInt(jampelajaran.getText()), Double.parseDouble(gajijam.getText()), Double.parseDouble(tunjangan.getText()), kettunjangan.getText(), Double.parseDouble(total.getText()));
                JOptionPane.showMessageDialog(null,"Data Gaji Guru berhasil dibuat");
                tabelGajiGuru();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null,"Data Gaji Guru gagal dibuat"+e.getMessage());
        }
    }//GEN-LAST:event_saveActionPerformed

